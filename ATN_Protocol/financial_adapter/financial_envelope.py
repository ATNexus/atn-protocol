import uuid
import json
import time

class FinancialEnvelope:
    """
    Encapsulates an external task request into a Financial Envelope
    with an ATN Task ID, Solana escrow details, and a transaction signature.
    Also simulates a locking timer for refund logic.
    """
    REFUND_TIMEOUT_SECONDS = 15 * 60 # 15 minutes for simulation

    def __init__(self):
        pass

    def wrap_request(self, external_payload: dict, financial_params: dict) -> dict:
        """
        Wraps an external task request with ATN financial context.
        :param external_payload: The original task request from an external platform (e.g., Claude, Perplexity).
        :param financial_params: Financial parameters (escrow_amount_sol, slashing_penalty_sol, min_sbt_score).
        :return: A dictionary representing the financial envelope.
        """
        atn_task_id = str(uuid.uuid4())
        
        # In a real Solana environment, these would be generated by a wallet/SDK
        # Here, we simulate them as placeholders.
        solana_escrow_address = f"ATNEscrow_{atn_task_id.replace('-', '')[:16]}"
        transaction_signature = f"TxSig_{uuid.uuid4().hex}"

        financial_envelope = {
            "atn_task_id": atn_task_id,
            "solana_escrow_details": {
                "escrow_address": solana_escrow_address,
                "escrow_amount_sol": financial_params.get("escrow_amount_sol", 0.0),
                "slashing_penalty_sol": financial_params.get("slashing_penalty_sol", 0.0),
                "min_sbt_score": financial_params.get("min_sbt_score", 0),
                "transaction_signature": transaction_signature,
                "locked_timestamp": time.time(), # Start of the lock timer
                "status": "escrow_locked",
                "refund_eligible_after": time.time() + self.REFUND_TIMEOUT_SECONDS
            },
            "original_request": external_payload
        }
        return financial_envelope

    def check_for_refund(self, financial_envelope: dict) -> dict:
        """
        Simulates checking if an escrowed task is eligible for refund due to timeout.
        In a real system, this would involve on-chain checks.
        """
        details = financial_envelope["solana_escrow_details"]
        current_time = time.time()

        if details["status"] == "escrow_locked" and current_time > details["refund_eligible_after"]:
            print(f"ATN_Protocol: Task {financial_envelope['atn_task_id']} timed out. Initiating refund process.")
            details["status"] = "refund_initiated"
            # In a real system, this would trigger a Solana transaction to unlock funds.
            details["refund_transaction_id"] = f"RefundTx_{uuid.uuid4().hex}"
        elif details["status"] == "escrow_locked":
            print(f"ATN_Protocol: Task {financial_envelope['atn_task_id']} is still active. Time remaining for delivery: {int(details['refund_eligible_after'] - current_time)} seconds.")
        else:
            print(f"ATN_Protocol: Task {financial_envelope['atn_task_id']} status is {details['status']}, not eligible for timeout refund check.")
        
        return financial_envelope

