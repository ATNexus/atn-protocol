    simulated_financial_params = {
        "escrow_amount_sol": 0.1,
        "slashing_penalty_sol": 0.02,
        "min_sbt_score": 100
    }

    envelope_maker = FinancialEnvelope()
    
    print("\\n--- 封裝跨平台請求 ---")
    wrapped_request_json = envelope_maker.wrap_request(external_claude_request, simulated_financial_params)
    print("被封装后的跨平台请求 JSON 範例:")
    print(json.dumps(wrapped_request_json, indent=4))

    # 模拟超时退款逻辑
    print("\\n--- 模拟超时退款逻辑 ---")
    # 首次检查：任务仍在锁定中，未超时
    updated_envelope = envelope_maker.check_for_refund(wrapped_request_json)
    print(json.dumps(updated_envelope["solana_escrow_details"], indent=4))

    # 模拟时间流逝，使其超过退款截止时间
